#program kalkulator sederhana
#fitur kalkulator penjumlahan pengurangn perkalian pembagian.

angka = int(input("angka yg ingin di jumlahkan :"))
kedua = int(input("angka ke dua :"))

print(f"hasil {angka} + {kedua} = {angka + kedua} hasil penjumlahan")
print(f"hasil {angka} - {kedua} = {angka - kedua} hasil pengurangan")
print(f"hasil {angka} * {kedua} = {angka * kedua} hasil perkalian")
print(f"hasil {angka} / {kedua} = {angka / kedua} hasil pembagian")

